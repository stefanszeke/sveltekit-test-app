  <p>In general use, herbs are a widely distributed and widespread group of plants, excluding vegetables and other plants consumed for macronutrients, with savory or aromatic properties that are used for flavoring and garnishing food, for medicinal purposes, or for fragrances. </p>
  <p>Culinary use typically distinguishes herbs from spices. Herbs generally refers to the leafy green or flowering parts of a plant, while spices are usually dried and produced from other parts of the plant, including seeds, bark, roots and fruits.</p>
  <p>Herbs have a variety of uses including culinary, medicinal, aromatic and in some cases, spiritual. General usage of the term "herb" differs between culinary herbs and medicinal herbs; in medicinal or spiritual use, any parts of the plant might be considered as "herbs", including leaves, roots, flowers, seeds, root bark, inner bark, resin and pericarp.</p>
